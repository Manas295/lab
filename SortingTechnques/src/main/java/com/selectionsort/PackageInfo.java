	/*How selection sort works
	 * 
	 * In selection sort aim is to bring the lowest element to the 0th index in the first pass. 
	 * In the next iteration the second lowest to the 1st index and so on.
	 * 
	 * You start with the element at the 0th index and consider it the lowest. 
	 * Then compare it with the other elements, if any element is smaller than the first element 
	 * then that element becomes the lowest for further comparisons in that pass. 
	 * That way you have the lowest element at the end of the iteration it is then swapped with the element at the 0th index.
	 * 
	 * Same way in the next iteration you start with the element at the 1st index and consider it the second lowest element. 
	 * Then compare it with all the elements on its right, in between if any element smaller than this element is found 
	 * then further comparisons are done using the now smallest element.
	 * 
	 * 
	 * 
	 */
